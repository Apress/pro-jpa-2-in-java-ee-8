DROP TABLE DEPARTMENT_EMPLOYEE;
DROP TABLE EMPLOYEE;
DROP TABLE DEPARTMENT;

CREATE TABLE DEPARTMENT (ID INTEGER NOT NULL, NAME VARCHAR(255), PRIMARY KEY (ID));
CREATE TABLE EMPLOYEE (ID INTEGER NOT NULL, FIRSTNAME VARCHAR(255), LASTNAME VARCHAR(255), FULLTIME INTEGER,
                       BONDED INTEGER, DATEBONDED DATE, HOMEPAGE VARCHAR(255), PRIMARY KEY (ID));
CREATE TABLE DEPARTMENT_EMPLOYEE (DEPARTMENT_ID INTEGER NOT NULL, EMPLOYEES_ID INTEGER NOT NULL,
                               CONSTRAINT DEPARTMENTS_FK FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENT (ID),
                               CONSTRAINT EMPLOYEES_FK FOREIGN KEY (EMPLOYEES_ID) REFERENCES EMPLOYEE (ID),
                               PRIMARY KEY (DEPARTMENT_ID, EMPLOYEES_ID));

