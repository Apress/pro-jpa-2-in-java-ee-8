DROP TABLE EMP;
DROP TABLE DEPARTMENT;
DROP TABLE ADDRESS;

CREATE TABLE DEPARTMENT (ID INTEGER, NAME VARCHAR(255), PRIMARY KEY (ID));
CREATE TABLE ADDRESS (ID INTEGER NOT NULL, STREET VARCHAR(255), CITY VARCHAR(255), 
                      STATE VARCHAR(255), ZIP VARCHAR(255), PRIMARY KEY (ID));
CREATE TABLE EMP (EMP_ID INTEGER, NAME VARCHAR(255), SALARY BIGINT, 
                  MANAGER_ID INTEGER, DEPT_ID INTEGER, ADDRESS_ID INTEGER, PRIMARY KEY (EMP_ID),
                  CONSTRAINT MGR_FK FOREIGN KEY (MANAGER_ID) REFERENCES EMP (EMP_ID),
                  CONSTRAINT ADDRESS_FK FOREIGN KEY (ADDRESS_ID) REFERENCES ADDRESS (ID),
                  CONSTRAINT DEPT_FK FOREIGN KEY (DEPT_ID) REFERENCES DEPARTMENT (ID));

