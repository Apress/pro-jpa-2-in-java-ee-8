DROP TABLE ID_GEN;

CREATE TABLE ID_GEN (GEN_NAME VARCHAR(80), GEN_VAL INTEGER, PRIMARY KEY (GEN_NAME));
CREATE TABLE PROJECT (ID INTEGER NOT NULL, NAME VARCHAR(255), PRIMARY KEY (ID));
CREATE TABLE DEPARTMENT (ID INTEGER NOT NULL, NAME VARCHAR(255), PRIMARY KEY (ID));
CREATE TABLE EMP (ID INTEGER NOT NULL, NAME VARCHAR(255), STARTDATE DATE, TYPE VARCHAR(32),
                     MGR INTEGER, DEPT INTEGER, DAILYRATE INTEGER, TERM INTEGER,
                     VACATION INTEGER, HOURLYRATE FLOAT, SALARY BIGINT, PENSION BIGINT, 
                     CONSTRAINT MGR_FK FOREIGN KEY (MGR) REFERENCES EMP(ID),
                     CONSTRAINT DEPARTMENT_FK FOREIGN KEY (DEPT) REFERENCES DEPARTMENT(ID),
                     PRIMARY KEY (ID));
CREATE TABLE EMP_ADDRESS (EMP_ID INTEGER NOT NULL, STREET VARCHAR(255), CITY VARCHAR(255), PROV VARCHAR(255), PCODE VARCHAR(255), PRIMARY KEY (EMP_ID));
CREATE TABLE EMP_PRJ(PROJ_ID INTEGER NOT NULL, EMP_ID INTEGER NOT NULL,
                        CONSTRAINT PROJECTS_FK FOREIGN KEY (PROJ_ID) REFERENCES PROJECT (ID),
                        CONSTRAINT EMPLOYEES_FK FOREIGN KEY (EMP_ID) REFERENCES EMP (ID),
                        PRIMARY KEY (PROJ_ID, EMP_ID));
CREATE TABLE PARKINGSPACE (ID INTEGER  NOT NULL, LOT INTEGER, LOCATION VARCHAR(255), EMP_ID INTEGER, PRIMARY KEY (ID));


